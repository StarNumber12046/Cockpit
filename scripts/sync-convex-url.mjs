/**
 * Copy CONVEX_URL from packages/backend/.env.local into
 * apps/mobile/.env as EXPO_PUBLIC_CONVEX_URL for Expo.
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const backendEnv = path.join(root, "packages", "backend", ".env.local");
const mobileEnv = path.join(root, "apps", "mobile", ".env");

if (!fs.existsSync(backendEnv)) {
  console.error(
    "Missing packages/backend/.env.local — run `pnpm convex:login` then `pnpm convex:setup` (or `pnpm --filter @cockpit/backend dev`) first.",
  );
  process.exit(1);
}

const text = fs.readFileSync(backendEnv, "utf8");
const match = text.match(/^\s*CONVEX_URL\s*=\s*(.+)\s*$/m);
if (!match) {
  console.error("CONVEX_URL not found in packages/backend/.env.local");
  process.exit(1);
}

const convexUrl = match[1].trim().replace(/^["']|["']$/g, "");
if (!convexUrl) {
  console.error("CONVEX_URL is empty");
  process.exit(1);
}

if (convexUrl.includes("127.0.0.1") || convexUrl.includes("localhost")) {
  console.warn(
    `Warning: CONVEX_URL looks local (${convexUrl}). Hosted Convex URLs look like https://<name>.convex.cloud`,
  );
}

let next = "";
if (fs.existsSync(mobileEnv)) {
  next = fs.readFileSync(mobileEnv, "utf8");
  if (/^\s*EXPO_PUBLIC_CONVEX_URL\s*=/m.test(next)) {
    next = next.replace(
      /^\s*EXPO_PUBLIC_CONVEX_URL\s*=.*$/m,
      `EXPO_PUBLIC_CONVEX_URL=${convexUrl}`,
    );
  } else {
    next = next.replace(/\s*$/, `\nEXPO_PUBLIC_CONVEX_URL=${convexUrl}\n`);
  }
} else {
  next = `# Generated by pnpm sync:convex-url\nEXPO_PUBLIC_CONVEX_URL=${convexUrl}\n`;
}

fs.writeFileSync(mobileEnv, next, "utf8");
console.log(`Wrote EXPO_PUBLIC_CONVEX_URL=${convexUrl} → apps/mobile/.env`);
